def GlyphToNumbers(glyphData : list) -> list:
  glyphAsNumbers = [];

  for line in glyphData:
    ln = len(line);

    lineAsNumber = 0;

    for i in range(ln):
      value = 0 if line[i] == '.' else 1;
      lineAsNumber = (lineAsNumber | (value << i));

    glyphAsNumbers.append(lineAsNumber);

    #print(f"{lineAsNumber:09b}");

  return glyphAsNumbers;

