def main():
  cppCode = (
    "using GlyphData = std::vector<std::vector<uint16_t>>;\n"
    "const GlyphData _charMap = \n"
    "{\n"
  );

  for glyph in Glyphs:
    res = GlyphToNumbers(glyph);

    vectorItem = "  { ";

    for value in res:
      vectorItem += f"{ value }, ";

    vectorItem = vectorItem[:-2];
    vectorItem += " },";

    cppCode += f"{ vectorItem }\n";

  cppCode += "};\n";

  print(cppCode);

